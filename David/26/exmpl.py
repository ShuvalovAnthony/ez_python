data = [
    # фамилия рус мат инф
    ["Огурцов", 67, 85, 85],
    ["Петров", 67, 82, 82],
    ["Апельсинов", 67, 45, 72],
    ["Помидоров", 71, 85, 85],
    ["Иванов", 67, 85, 85],
    ["Столов", 72, 76, 72],
    ["Петрушкин", 71, 77, 72],
]

# поступаем в инф вуз - приоритет
# 1) сумма баллов
# 2) информатика
# 3) математика
# 4) русский
# 5) по алфавиту

data = sorted(data, key=lambda x: (
    -sum(x[1:]),
    -x[3],
    -x[2],
    -x[1],
    x[0]
))


for row in data:
    print(*row)